function changToGangster(){
    /* ค้นหา แท็กที่มี id mainImage ใน html */
    var image = document.getElementById("mainImage");
    /* ค้นหา แท็กที่มี id friendName ใน html */
    var name = document.getElementById("friendName");

    //เปลี่ยน คำของมัน
    image.src ="group.jpg";
    name.innerHTML ="Gangster"
}

